""" Write a python script to check whether two given strings are identical
first string comes before the second in dictionary order or first string
comes after the secondstring in dictionary order
using match case statement """

x = "patel"
y = "jayesh"

match x:
    case x:
        if x > y:
            print("first string comes before the second string")
            print()
        else:
            print("first string comes after the second string")
            print()
